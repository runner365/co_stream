    uint32_t GetCrc32(uint32_t* crc_table, uint32_t crc, const uint8_t* data, size_t size)
    {
        const uint8_t *end = data + size;
        while (data < end) {
            crc = crc_table[((uint8_t) crc) ^ *data++] ^ (crc >> 8);
        }
        return crc;
    }
